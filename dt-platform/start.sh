#!/bin/bash

# ------------------------------------------------------
# DT 开发平台一键启动脚本 (Shell Script)
# ------------------------------------------------------

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
cd "$SCRIPT_DIR" || exit 1

echo "=================================================="
echo "🚀 启动 DT 开发平台：开始执行环境依赖扫描..."
echo "=================================================="

# 1. 扫描数据 (强制执行，确保数据是最新的)
echo "--- 步骤 1/3: 扫描本地文档目录并生成 manifest.json ---"
node scripts/gen-doc-tree.js

# 检查上一步是否成功，如果失败则退出
if [ $? -ne 0 ]; then
    echo "❌ 脚本执行失败！无法生成文档清单。请检查 Mac 路径配置是否正确。"
    exit 1
fi

echo ""
echo "=================================================="
echo "✨ 步骤 2/3: 启动本地静态文件服务器..."
echo "=================================================="

# 2. 启动内置本地服务，负责静态页面和 Markdown 文档内联预览
PORT="${PORT:-8082}"
echo "🌐 预设端口: $PORT"
PORT="$PORT" node server.js

# 3. (可选) 提示用户访问地址
echo ""
echo "=================================================="
echo "✅ 启动完成！请在浏览器中访问：http://localhost:$PORT"
echo "=================================================="
